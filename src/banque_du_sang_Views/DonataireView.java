    /* Methode to Show Data in table and also add them to List */
    public void Afficher(){
        try {
            Statement st = conn.createStatement();
            String req = "select * from Donataire ";
            if(order == "Nouveau"){
                req+= " order by DonataireID desc";
            }else if(order == "Ancien"){
                req+= " order by DonataireID asc";
            }
            ResultSet rs  = st.executeQuery(req);
            df.setRowCount(0);
            while(rs.next()){
                donataire.add(new Donataire(rs.getInt(1),rs.getString(2),rs.getString(3), rs.getDate(4), rs.getString(5), rs.getString(6),rs.getString(7),rs.getInt(8)));
                df.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getString(3), rs.getDate(4), rs.getString(5), rs.getString(6),rs.getString(7),rs.getInt(8)});
            }
            
            rs.close();
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    
    /* Remplir les input pour la modification */
    public void remplirInputs(){
        Donataire d = donataire.get(row);
        txtNomDonataire.setText(d.getDonataireNom());
        txtDateNaiss.setText(d.getDonataireDateNaiss()+"");
        txtAdresseDonataire.setText(d.getDonataireAdresse());
        txtTeleDonataire.setText(d.getDonataireTele()+"");
        txtVilleDonataire.setText(d.getDonataireVille());
        String gender = d.getDonataireSexe();
        if(gender.equals("Male")){
            jrbtnMale.setSelected(true);
        }else{
            jrbtnFemale.setSelected(true);
        }
        jcmbTypeSang.setSelectedItem(d.getDonataireTypeSang());
    }
    
    

