    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String nom = txtNomDonataire.getText();
        String gender;
        if(jrbtnMale.isSelected()){
            gender="Male";
        }else{
            gender="Female";
        }
        String adresse = txtDateNaiss.getText();
        String typeSang = jcmbTypeSang.getSelectedItem().toString();
        String ville = txtVilleDonataire.getText();
        java.sql.Date date = java.sql.Date.valueOf(txtDateNaiss.getText());
        int tele = Integer.parseInt(txtTeleDonataire.getText());
        PreparedStatement ps = null;
        try {
            if(action.equals("ajouter")){
                ps = conn.prepareStatement("insert into Donataire values(seq_DonataireID.nextval,?,?,?,?,?,?,?)");
                ps.setString(1, nom);
                ps.setString(2, gender);
                ps.setDate(3, date);
                ps.setString(4, typeSang);
                ps.setString(5, adresse);
                ps.setString(6, ville);
                ps.setInt(7, tele);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Donataire a ete ajoute avec succes");
                txtNomDonataire.setText("");
                txtDateNaiss.setText("");
                txtAdresseDonataire.setText("");
                txtTeleDonataire.setText("");
                txtVilleDonataire.setText("");
            }else if(action.equals("modifier")){
                ps = conn.prepareStatement("update Donataire set DonataireNom=?, DonataireSexe=?, DonataireDateNaiss=?, DonataireTypeSang=?, DonataireAdresse=?, DonataireVille=?, DonataireTele=? where DonataireID=?");
                Donataire d = donataire.get(row);
                ID = d.getDonataireID();
                ps.setString(1, nom);
                ps.setString(2, gender);
                ps.setDate(3, date);
                ps.setString(4, typeSang);
                ps.setString(5, adresse);
                ps.setString(6, ville);
                ps.setInt(7, tele);
                ps.setInt(8, ID);
                ps.executeUpdate();
                d.setDonataireNom(nom);
                d.setDonataireSexe(gender);
                d.setDonataireDateNaiss(date);
                d.setDonataireTypeSang(typeSang);
                d.setDonataireAdresse(adresse);
                d.setDonataireVille(ville);
                d.setDonataireTele(tele);
                JOptionPane.showMessageDialog(null, "Donataire a ete modifie avec succes");
            }
            
            Afficher();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

