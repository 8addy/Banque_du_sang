    private void btnSupprimerDonataireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerDonataireActionPerformed
        int agree = JOptionPane.showConfirmDialog(null, "Voulez Vous supprimer cette Details?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if(agree==JOptionPane.YES_OPTION){
            DetailsDonation d = details.get(row);
            ID = d.getDonationNum();
            try {
                PreparedStatement ps  = conn.prepareStatement("delete from DetailsDonation where DonationNum=?");
                ps.setInt(1, ID);
                ps.executeQuery();
                details.remove(d);
                Afficher();
                JOptionPane.showMessageDialog(null, "Details a ete supprimer avec succes");
            } catch (SQLException ex) {
                Logger.getLogger(DetailsDonationView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSupprimerDonataireActionPerformed

    private void btnSearchInputMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchInputMouseClicked
        String nom = txtSearchNom.getText();
            df.setRowCount(0);
        try {
            PreparedStatement ps = conn.prepareStatement("select * from Donataire where lower(DonataireNom) like lower(?)");
            ps.setString(1,"%"+nom+"%");
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                df.addRow(new Object[]{rs.getInt(1),rs.getInt(2),rs.getInt(3), rs.getString(4), rs.getDate(5), rs.getInt(6)});
            }
            AfficherTous.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(DetailsDonationView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchInputMouseClicked

