    /* Methode to Show Data in table and also add them to List */
    public void Afficher(){
        try {
            Statement st = conn.createStatement();
            String req = "select * from DetailsDonation ";
            if(order == "Nouveau"){
                req+= " order by DonationNum desc";
            }else if(order == "Ancien"){
                req+= " order by DonationNum asc";
            }
            ResultSet rs  = st.executeQuery(req);
            df.setRowCount(0);
            while(rs.next()){
                details.add(new DetailsDonation(rs.getInt(1),rs.getInt(2),rs.getInt(3), rs.getString(4), rs.getDate(5), rs.getInt(6)));
                df.addRow(new Object[]{rs.getInt(1),rs.getInt(2),rs.getInt(3), rs.getString(4), rs.getDate(5), rs.getInt(6)});
            }
            
            rs.close();
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    
    
    
    

